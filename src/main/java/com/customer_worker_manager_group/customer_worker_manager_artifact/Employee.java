package com.customer_worker_manager_group.customer_worker_manager_artifact;

public class Employee {

    private char[] name;
    private char[] status;
    private char[] startDate;
    private char[] endDate;
    private char[] companyID;

    public char[] getName() {
        return name;
    }

    public char[] getStatus() {
        return status;
    }

    public char[] getStartDate() {
        return startDate;
    }

    public char[] getEndDate() {
        return endDate;
    }

    public char[] getCompanyID() {
        return companyID;
    }

}
